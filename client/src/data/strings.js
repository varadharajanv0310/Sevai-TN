export const t = (key, lang = 'ta') => {
  const entry = STRINGS[key];
  if (!entry) return key;
  return entry[lang] ?? entry.ta ?? key;
};

// Template helper
export const tf = (key, lang, vars = {}) => {
  let s = t(key, lang);
  Object.entries(vars).forEach(([k, v]) => {
    s = s.replaceAll(`{${k}}`, String(v));
  });
  return s;
};
